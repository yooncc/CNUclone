	<div class="con-box no-pd">
		<h4 class="h4-tit01">
			학과소개</h4>
		<p class="con-p01">
			수학은 인간의 지적활동의 근간에 속하는 분류, 계산, 추측, 증명 등에 관한 능력을 활용하여 수와 공간, 또 그 위에서의 현상을 나타내는 함수 등을 연구하는 학문이다. 이런 수학은 자연현상을 이해하는 과학의 언어이며, 최근에는 인문과학과 사회과학에까지 다양한 현상과 복잡한 관계를 연구하는 효과적인 수단이 되고 있다.</p>
		<p class="con-p01">
			수학은 연구 대상이나 방법에 따라 기하학, 대수학, 위상학, 해석학, 수치해석학, 통계학, 컴퓨터 과학, 계산수학, 금융수학, 생물수학 등의 여러 분야로 나누어지며, 이 분야들은 독립적인 연구 영역을 가지는 반면, 공동의 연구대상을 다른 방법으로 연구하기도 한다.</p>
		<p class="con-p01">
			2002년 한국대학교육협의회에서 실시하는 수학 학문평가에서 최우수상을 수상한 수학과는 학부과정에 360여명의 학생이 재학 중이며, 50여명의 석박사과정생이 16명의 교수진과 BK21 과제를 포함하여 연구재단의 여러 과제를 수행하면서 연구에 정진하고 있다.</p>
		<p class="con-p01">
			수학과의 교육목표는 우리 학생들이 질높은 수학 교육을 통하여 수학적 지식 및 수학적 사고 능력을 길러 다양한 교육기관에서 교사와 연구원으로서, 그리고 우리 사회의 수학을 활용하는 여러 분야에 진출하여 수학적 재능을 최대한 발휘토록 하는 것이다.</p>
		<p class="con-p01">
			수학과는 현재 발행되는 수학전문잡지를 비롯한 만 여권 이상의 학술자료 및 수학전문잡지가 비치되어 있는 자료실을 직접 관리하고 개방하고 있고, PC 100여대와 강의용 전자장비를 활용한 첨단강의실과 대학원전용 PC Lab과 학부생전용 PC Lab도 운영하고 있다.</p>
		<p class="con-p01">
			학생들이 수학을 활용하는 여러 분야에의 진출을 용이하게 하고자 Matlab을 비롯한 여러 수학전용 소프트웨어를 활용한 수업을 강화하고 있으며, 학생의 진로와 능력에 따라서 차별화된 교육을 지향하고자 한다. 수학과에는 취업준비실을 따로 운영하면서 취업준비생 및 창업준비생을 지원하고 있으며, 학술동아리 F.O.M(Fan Of Math), 금융수학연구실 등 활발한 활동을 하고 있는 상황이다.</p>
		<p class="con-p01">
			현재 수학과에서는 여러 형태의 교내장학금(우수, 격려 등)과 국가장학금(이공계, 미래드림, 희망드림 등), 수학과 학생들만 수혜가능한 감민호 수학과장학금 및 수학과 교수 장학금 그리고 그 외에도 여러 외부장학금이 있어, 재학생의 약 50%이상이 장학금을 받고 있다.</p>
		<p class="con-p01">
			현재 수학과에서는 여러 형태의 교내장학금(우수, 격려 등)과 국가장학금(이공계, 미래드림, 희망드림 등), 수학과 학생들만 수혜가능한 감민호 수학과장학금 및 수학과 교수 장학금 그리고 그 외에도 여러 외부장학금이 있어, 재학생의 약 50%이상이 장학금을 받고 있다.</p>
		<p class="con-p01 no-pd">
			수학과를 졸업한 후에는 국내외의 대학원을 마친 후 여러 연구기관의 연구원 또는 교수로 재직하거나, 본교의 교육대학원으로 진학하여 중등학교 교사로의 진출도 가능하며, 교육연구기관, 금융기관, 공사, IT계열 소프트웨어 개발 및 서버 구축 프로젝트 팀 등으로 많이 진출하고 있다.</p>
